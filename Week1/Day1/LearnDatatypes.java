package week1.day1;

public class LearnDatatypes {
	public static void main(String[] args) {
		//byte
		byte age=31;
		
		//short
		short amount=30000;
		
		//int
		int rupee=50000;
		
		long mobileNumber=8925411170L;
		
		//float
		float rateOfInterest=2.25F;
		
		float rupeeToDollar=1.123456789F;
		System.out.println(rupeeToDollar);
		
		double dollar=1.123456789;
		System.out.println(dollar);
		
		//Char - To store single Character - Alpha, Numbers, Special
		char logo='T';
		
		//boolean - true or false
		boolean doYouKnowJava=false;
		
		//String
		String name="Vineeth";
		
		//Print 2 Variables
		System.out.println(name+" "+age);
		
		//Print 1 Variable and 1 String
		System.out.println("My name is "+name);
	}
}
